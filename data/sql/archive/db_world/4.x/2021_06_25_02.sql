CREATE PROCEDURE updateDb ()
proc:BEGIN DECLARE OK VARCHAR(100) DEFAULT 'FALSE';
SELECT COUNT(*) INTO @COLEXISTS
FROM information_schema.COLUMNS
WHERE TABLE_SCHEMA = DATABASE() AND TABLE_NAME = 'version_db_world' AND COLUMN_NAME = '2021_06_25_01';
IF @COLEXISTS = 0 THEN LEAVE proc; END IF;
START TRANSACTION;
ALTER TABLE version_db_world CHANGE COLUMN 2021_06_25_01 2021_06_25_02 bit;
SELECT sql_rev INTO OK FROM version_db_world WHERE sql_rev = '1624013817174597427'; IF OK <> 'FALSE' THEN LEAVE proc; END IF;
--
-- START UPDATING QUERIES
--

INSERT INTO `version_db_world` (`sql_rev`) VALUES ('1624013817174597427');

-- Slow Baron Charr down to reasonable speed
UPDATE `creature_template` SET `speed_walk` = 1 WHERE `entry` = 14461;

--
-- END UPDATING QUERIES
--
UPDATE version_db_world SET date = '2021_06_25_02' WHERE sql_rev = '1624013817174597427';
COMMIT;
END //
DELIMITER ;
